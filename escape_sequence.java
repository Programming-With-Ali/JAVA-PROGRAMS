class escape_sequence {
    public static void main(String[] args) {
        System.out.println(" newline escape character");
        System.out.println("Hey there this Ali\n" + "Ali");
        System.out.println(" backspace escape character");
        System.out.println("Hey there this Ali\b");
        System.out.println(" tab escape character");
        System.out.println("Hey there this Ali \t");
        System.out.println(" single qoute escape character");
        System.out.println("Hey there this Ali \'");
        System.out.println(" double qoute escape character");
        System.out.println("Hey there this Ali \"");
        System.out.println(" This is backslash n used to change line");
        System.out.println("Hey there this Ali \r");
        System.out.println("Double qoute \"");
        System.out.println(" carriage return qoute escape character");
        System.out.println("Hey there this Ali \\\\");
        System.out.println(" back slash escape character");
        System.out.println("Hey there this Ali \\");
    }
}
