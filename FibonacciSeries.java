class FibonacciSeries {
    public static void main(String[] args) {
        while (true) {
            int a = 0, b = 1, c;
            for (int i = 1; i < 13; i++) {
                System.out.println(" " + a);
                c = a + b;
                a = b;
                b = c;
            }
        }
    }
}
